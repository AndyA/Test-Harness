=head2 Example

  package MyPerlSourceDetector;

  use strict;
  use vars '@ISA';

  use Carp qw( croak );
  use TAP::Parser::SourceDetector::Perl;

  @ISA = qw( TAP::Parser::SourceDetector::Perl );

  sub raw_source {
      my ($self, $args) = @_;
      if ($args) {
	  $self->{file} = $args->[0];
	  return $self->SUPER::raw_source($args);
      }
      return $self->SUPER::raw_source;
  }

  # use the version of perl from the shebang line in the test file
  sub _get_perl {
      my $self = shift;
      if (my $shebang = $self->shebang( $self->{file} )) {
          $shebang =~ /^#!(.*\bperl.*?)(?:(?:\s)|(?:$))/;
	  return $1 if $1;
      }
      return $self->SUPER::_get_perl(@_);
  }

