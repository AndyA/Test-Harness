=head1 NAME

TAP::Parser::SourceDetector::Perl - Stream TAP from a Perl executable

=head1 VERSION

Version 3.18

=cut

$VERSION = '3.18';

=head1 SYNOPSIS

  use TAP::Parser::SourceDetector::Perl;
  my $perl = TAP::Parser::SourceDetector::Perl->new;
  my $stream = $perl->raw_source( [ $filename, @args ] )->get_stream;

=head1 DESCRIPTION

This is a I<Perl> L<TAP::Parser::SourceDetector> - it has 2 jobs:

1. Figure out if the I<raw> source it's given is actually a Perl script.  See
L<TAP::Parser::SourceFactory> for more details.

2. Takes a Perl script and creates an iterator from it.

Unless you're writing a plugin or subclassing L<TAP::Parser>, you probably
won't need to use this module directly.

=head1 METHODS

=head2 Class Methods

=head3 C<new>

 my $perl = TAP::Parser::SourceDetector::Perl->new;

Returns a new C<TAP::Parser::SourceDetector::Perl> object.

=cut

sub _initialize {
    my ( $self, @args ) = @_;
    $self->SUPER::_initialize(@args);
    $self->{switches} = [];
    return $self;
}


=head3 C<can_handle>

=cut

sub can_handle {
    my ( $class, $src ) = @_;
    my $meta = $src->meta;

    return 0 unless $meta->{is_file};
    my $file = $meta->{file};

    if (my $shebang = $file->{shebang}) {
	return 0.99 if $shebang =~ /^#!.*\bperl/;
    }

    return 0.8 if $file->{lc_ext} eq '.t';    # vote higher than Executable
    return 1   if $file->{lc_ext} eq '.pl';

    return 0.75 if $file->{dir} =~ /^t\b/;    # vote higher than Executable

    # backwards compat, always vote:
    return 0.5;
}

=head3 C<make_source>

=cut

sub make_source {
    my ( $class, $src ) = @_;
    my $meta = $src->meta;
    my $perl_script = ${ $src->raw };
    my $test_args   = $src->test_args || [];

    my $source = $class->new( $src->raw );
    $source->merge( $src->merge );
    $source->switches( $src->switches ) if $src->switches;
    $source->raw_source( [ $perl_script, @$test_args ] );

    return $source;
}

=head2 Instance Methods

=head3 C<raw_source>

Getter/setter the name of the test program and any arguments it requires.

  my ($filename, @args) = @{ $perl->raw_source };
  $perl->raw_source( [ $filename, @args ] );

C<croak>s if C<$filename> could not be found.

=cut

sub raw_source {
    my $self = shift;
    $self->_croak("Cannot find ($_[0][0])")
      if @_ && !-f $_[0][0];
    return $self->SUPER::raw_source(@_);
}

=head3 C<switches>

  my $switches = $perl->switches;
  my @switches = $perl->switches;
  $perl->switches( \@switches );

Getter/setter for the additional switches to pass to the perl executable.  One
common switch would be to set an include directory:

  $perl->switches( ['-Ilib'] );

=cut

sub switches {
    my $self = shift;
    unless (@_) {
        return wantarray ? @{ $self->{switches} } : $self->{switches};
    }
    my $switches = shift;
    $self->{switches} = [@$switches];    # force a copy
    return $self;
}

